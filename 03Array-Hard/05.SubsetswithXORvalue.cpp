class Solution{
public:
    int subsetXOR(vector<int> arr, int N, int K) {
        // code here
        int xr = 0;
    map<int, int> mpp; //declaring the map.
    mpp[xr]++; //setting the value of 0.
    int cnt = 0;

    for (int i = 0; i < N; i++) {
        // prefix XOR till index i:
        xr = xr ^ arr[i];

        //By formula: x = xr^k:
        int x = xr ^ K;

        // add the occurrence of xr^k
        // to the count:
        cnt += mpp[x];

        // Insert the prefix xor till index i
        // into the map:
        mpp[xr]++;
    }
    return cnt;
    }
};

